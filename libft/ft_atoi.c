int		ft_atoi(char *str)
{
	int i;
	int hold;
	int neg;

	i = 0;
	hold = 0;
	neg = 1;
	while ((str[i] == ' ') || (str[i] < 32 && str[i] > 23) || (str[i] == '+'))
	{
		i++;
	}
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		hold *= 10;
		hold += str[i] - '0';
		i++;
	}
	return (hold * neg);
}
