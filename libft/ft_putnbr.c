#include "libft.h"
#include <unistd.h>

void	ft_putnbr(int n)
{
	long int i;

	i = n;
	if (i < 0)
	{
		i = i * (-1);
		ft_putchar('-');
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		ft_putchar('0' + i);
}
