#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	int i;
	int j;

	i = ft_strlen(s1);
	j = 0;
	while (j < len && s2[j])
	{
		s1[i + j] = s2[j];
		j++;
	}
	s1[i + j] = '\0';
	return (s1);
}
